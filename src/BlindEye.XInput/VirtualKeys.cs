namespace BlindEye.XInput
{
    /// <summary>
    /// Specifies constants that represent a virtual key.
    /// </summary>
    public enum VirtualKeys : ushort
    {
        /// <summary>
        /// Indicates the gamepad A button.
        /// </summary>
        GamepadA = 0x5800,

        /// <summary>
        /// Indicates the gamepad B button.
        /// </summary>
        GamepadB = 0x5801,

        /// <summary>
        /// Indicates the gamepad X button.
        /// </summary>
        GamepadX = 0x5802,

        /// <summary>
        /// Indicates the gamepad Y button.
        /// </summary>
        GamepadY = 0x5803,

        /// <summary>
        /// Indicates the gamepad right shoulder.
        /// </summary>
        GamepadRightShoulder = 0x5804,

        /// <summary>
        /// Indictates the gamepad left shoulder.
        /// </summary>
        GamepadLeftShoulder = 0x5805,

        /// <summary>
        /// Indicates the gamepad left trigger.
        /// </summary>
        GamepadLeftTrigger = 0x5806,

        /// <summary>
        /// Indicates the gamepad right trigger.
        /// </summary>
        GamepadRightTrigger = 0x5807,

        /// <summary>
        /// Indicates the gamepad directional-pad up.
        /// </summary>
        GamepadDPadUp = 0x5810,

        /// <summary>
        /// Indicates the gamepad directional-pad down.
        /// </summary>
        GamepadDPadDown = 0x5811,

        /// <summary>
        /// Indicates the gamepad directional-pad left.
        /// </summary>
        GamepadDPadLeft = 0x5812,

        /// <summary>
        /// Indicates the gamepad directional-pad right.
        /// </summary>
        GamepadDPadRight = 0x5813,

        /// <summary>
        /// Indicates the gamepad start button.
        /// </summary>
        GamepadStart = 0x5814,

        /// <summary>
        /// Indicates the gamepad back button.
        /// </summary>
        GamepadBack = 0x5815,

        /// <summary>
        /// Indicates the left thumbstick button.
        /// </summary>
        GamepadLeftThumbstickPress = 0x5816,

        /// <summary>
        /// Indicates the right thumbstick button.
        /// </summary>
        GamepadRightThumbstickPress = 0x5817,

        /// <summary>
        /// Indicates the gamepad left thumbstick up.
        /// </summary>
        GamepadLeftThumbstickUp = 0x5820,

        /// <summary>
        /// Indicates the gamepad left thumbstick down.
        /// </summary>
        GamepadLeftThumbstickDown = 0x5821,

        /// <summary>
        /// Indicates the gamepad left thumbstick right.
        /// </summary>
        GamepadLeftThumbstickRight = 0x5822,

        /// <summary>
        /// Indicates the gamepad left thumbstick left.
        /// </summary>
        GamepadLeftThumbstickLeft = 0x5823,

        /// <summary>
        /// Indicates the gamepad left thumbstick up and left.
        /// </summary>
        GamepadLeftThumbstickUpLeft = 0x5824,

        /// <summary>
        /// Indicates the gamepad left thumbstick up and right.
        /// </summary>
        GamepadLeftThumbstickUpRight = 0x5825,

        /// <summary>
        /// Indicates the gamepad left thumbstick down and right.
        /// </summary>
        GamepadLeftThumbstickDownRight = 0x5826,

        /// <summary>
        /// Indicates the gamepad left thumbstick down and left.
        /// </summary>
        GamepadLeftThumbstickDownLeft = 0x5827,

        /// <summary>
        /// Indicates the gamepad right thumbstick up.
        /// </summary>
        GamepadRightThumbstickUp = 0x5830,

        /// <summary>
        /// Indicates the gamepad right thumbstick down.
        /// </summary>
        GamepadRightThumbstickDown = 0x5831,

        /// <summary>
        /// Indicates the gamepad right thumbstick right.
        /// </summary>
        GamepadRightThumbstickRight = 0x5832,

        /// <summary>
        /// Indicates the gamepad right thumbstick left.
        /// </summary>
        GamepadRightThumbstickLeft = 0x5833,

        /// <summary>
        /// Indicates the gamepad right thumbstick up and left.
        /// </summary>
        GamepadRightThumbstickUpLeft = 0x5834,

        /// <summary>
        /// Indicates the gamepad right thumbstick up and right.
        /// </summary>
        GamepadRightThumbstickUpRight = 0x5835,

        /// <summary>
        /// Indicates the gamepad right thumbstick down and right.
        /// </summary>
        GamepadRightThumbstickDownRight = 0x5836,

        /// <summary>
        /// Indicates the gamepad right thumbstick down and left.
        /// </summary>
        GamepadRightThumbstickDownLeft = 0x5837,
    }
}